package com.city.oa.value;

public class DepartmentValue {

	private int depid=0;
	private String depname=null;
	private String depaddr=null;
	private int deptel=0;
	
	
	public int getDepid() {
		return depid;
	}
	public void setDepid(int depid) {
		this.depid = depid;
	}
	public String getDepname() {
		return depname;
	}
	public void setDepname(String depname) {
		this.depname = depname;
	}
	public String getDepaddr() {
		return depaddr;
	}
	public void setDepaddr(String depaddr) {
		this.depaddr = depaddr;
	}
	public int getDeptel() {
		return deptel;
	}
	public void setDeptel(int deptel) {
		this.deptel = deptel;
	}
	
	
	
	
}
